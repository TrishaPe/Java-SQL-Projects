package modelos;

public class Cita {
    
    String notas;
    String fecha;
    int animal_cod;
    String especie;

    public int getAnimal_cod() {
        return animal_cod;
    }

    public void setAnimal_cod(int animal_cod) {
        this.animal_cod = animal_cod;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }
    
    
}
