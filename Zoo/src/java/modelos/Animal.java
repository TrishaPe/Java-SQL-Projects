package modelos;

public class Animal {
    
    int animal_cod;
    int dept_cod;
    String departamento;
    String nombre;
    String sexo;
    String especie;
    String dieta;
    String notas;
    int emp_cod;
    String empleado;
    String foto;

    

    public int getAnimal_cod() {
        return animal_cod;
    }

    public void setAnimal_cod(int animal_cod) {
        this.animal_cod = animal_cod;
    }

    public int getDept_cod() {
        return dept_cod;
    }

    public void setDept_cod(int dept_cod) {
        this.dept_cod = dept_cod;
    }
    
    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public String getDieta() {
        return dieta;
    }

    public void setDieta(String dieta) {
        this.dieta = dieta;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }

    public int getEmp_cod() {
        return emp_cod;
    }

    public void setEmp_cod(int emp_cod) {
        this.emp_cod = emp_cod;
    }
    
    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getEmpleado() {
        return empleado;
    }

    public void setEmpleado(String empleado) {
        this.empleado = empleado;
    }
    
}
